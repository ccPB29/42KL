void	ft_sort_int_tab(int *tab, int size)
{
	int	t;
	int	i;

	while(size){
i = 0;
while(i < size)
	{
		if(tab[i] > tab[i + 1])
		{
			t = tab[i];
			tab[i] = tab[i + 1];
			tab[i + i] = t;
		}
		i++;
	}
	size--;
	}
}